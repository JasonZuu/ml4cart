def main(argv=None) -> int:
    args = parse_args(argv)
    version = str(args.version)
    mask_names = VERSION_TO_MASKS[version]

    checkpoint = Path(args.model_dir) / version / "best_model.pth"
    out_dir = Path(args.output_dir) / version / "r4_drug_eval"
    out_dir.mkdir(parents=True, exist_ok=True)

    split_payload = load_split_json(args.split_json)
    transformations = MaskImageTransformations(
        image_size=int(args.image_size), normalize_mean=0.5, normalize_std=0.5
    )

    device = resolve_device(str(args.device))
    model = PDOChangeResNetClassifier(
        in_channels=len(mask_names),
        num_classes=len(PDO_CHANGE_BIN_LABELS),
        hidden_dim=int(args.hidden_dim),
        dropout=float(args.dropout),
    ).to(device)
    state = torch.load(checkpoint, map_location="cpu")
    model.load_state_dict(state, strict=True)
    print(f"Loaded checkpoint: {checkpoint}")

    pdo_labels = _load_pdo_labels(args.split_json)
    print(f"Loaded PDO labels: {len(pdo_labels)} entries")

    all_records: list[dict] = []

    for group_key in PATIENT_GROUPS:
        patient_label = group_key.replace("test_", "")  # "nyu285", "nyu318", "nyu774"
        case_ids = split_payload.get(group_key, []) or []
        if not case_ids:
            print(f"[warn] No case IDs for {group_key}")
            continue

        labeled_ids = [cid for cid in case_ids if cid in pdo_labels]
        if not labeled_ids:
            print(f"[warn] No labeled IDs for {patient_label}")
            continue

        dataset = OnchipPDOChangeDataset(
            masks_dir=args.masks_dir,
            image_ids=labeled_ids,
            pdo_change_labels=pdo_labels,
            mask_names=mask_names,
            transform=transformations.validation_transformations,
        )
        print(f"\n--- {patient_label}: {len(dataset)} samples loaded ---")

        records = _run_inference(model, dataset, device)
        all_records.extend(records)

    if not all_records:
        print("[error] No records produced.")
        return 1

    # Group by drug for summary plots / CSV
    drug_records: dict[str, list[dict]] = {drug: [] for drug in DRUG_ORDER}
    for rec in all_records:
        _, drug_disp, _ = _parse_case_id(rec["image_id"])
        drug_records[drug_disp].append(rec)

    # Group by patient for per-patient average prob plots
    patient_records: dict[str, list[dict]] = {}
    for rec in all_records:
        patient_str, _, _ = _parse_case_id(rec["image_id"])
        patient_key = f"nyu{patient_str}"
        patient_records.setdefault(patient_key, []).append(rec)

    save_prob_csv(all_records, out_dir / "prob_distribution.csv")
    save_predictions_csv(all_records, pdo_labels, out_dir / f"test_predictions_{version}.csv")
    plot_patient_avg_prob_distributions(patient_records, pdo_labels, out_dir, version)
    plot_pred_bin_summary(drug_records, pdo_labels, out_dir, version)
    print_summary(drug_records, pdo_labels)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
