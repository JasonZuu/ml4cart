VERSION_TO_MASKS = {
    "cd8_only":    ["cd8"],
    "cd8_actin_ck": ["cd8", "actin", "ck"],
    "actin_ck": ["actin", "ck"],
}

PATIENT_GROUPS = ["test_nyu285", "test_nyu318", "test_nyu774"]
DRUG_COLORS = {"FAP": "#e41a1c", "IgG": "#377eb8", "iAREG": "#4daf4a"}
DRUG_ORDER   = ["FAP", "IgG", "iAREG"]

# Semantic bin groups for plots
GROUPS = [
    ("Shrinkage  (PDO < −20%)",      0,  4, "Reds"),
    ("Near-zero  (−20 ≤ PDO < 20%)", 5,  6, "Blues"),
    ("Growth     (PDO ≥ 20%)",        7, 11, "Greens"),
]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _load_pdo_labels(split_json_path: Path) -> dict[str, float]:
    """Load pdo_change_label.json from the same directory as data_split.json."""
    label_path = split_json_path.parent / "pdo_change_label.json"
    with label_path.open("r", encoding="utf-8") as f:
        return json.load(f)


def _parse_case_id(cid: str) -> tuple[str, str, str]:
    """Return (patient, drug_display, day) from chip-r4_nyu{p}_{d}-{day}."""
    after = cid.split("chip-r4_nyu", 1)[1]
    patient, drug_day = after.split("_", 1)
    drug_lower, day = drug_day.split("-", 1)
    drug_display = {"fap": "FAP", "igg": "IgG", "iareg": "iAREG"}.get(drug_lower, drug_lower)
    return patient, drug_display, day


