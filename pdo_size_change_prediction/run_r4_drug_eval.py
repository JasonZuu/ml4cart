def parse_args(argv=None):
    parser = argparse.ArgumentParser(
        description="Evaluate pretrained PDO-change classifier on Chip-R4 drug groups."
    )
    parser.add_argument(
        "--version",
        choices=sorted(VERSION_TO_MASKS.keys()),
        default="actin_ck",
    )
    parser.add_argument("--masks-dir", type=Path, default=Path("data/On-chip_Data"))
    parser.add_argument(
        "--split-json", type=Path,
        default=Path("data/On-chip_Data/data_split.json"),
    )
    parser.add_argument(
        "--model-dir", type=Path,
        default=Path("onchip_pdochange_prediction/results"),
        help="Root results directory; version subdir appended automatically",
    )
    parser.add_argument(
        "--output-dir", type=Path,
        default=Path("onchip_pdochange_prediction/results"),
        help="Root output directory; version/r4_drug_eval subdir appended automatically",
    )
    parser.add_argument("--hidden-dim", type=int, default=256)
    parser.add_argument("--dropout", type=float, default=0.2)
    parser.add_argument("--image-size", type=int, default=512)
    parser.add_argument("--device", type=str, default="cuda")
    return parser.parse_args(argv)


# ---------------------------------------------------------------------------
# Inference
# ---------------------------------------------------------------------------

@torch.no_grad()
def _run_inference(
    model: PDOChangeResNetClassifier,
    dataset: OnchipPDOChangeDataset,
    device: torch.device,
) -> list[dict]:
    model.eval()
    records = []
    for idx in range(len(dataset)):
        image_tensor, true_label, image_id = dataset[idx]
        inp = image_tensor.unsqueeze(0).to(device)
        logits = model(inp)
        probs = torch.softmax(logits, dim=1)[0].cpu().numpy()
        pred_bin = int(logits.argmax(dim=1).item())
        true_bin = int(true_label.item())
        records.append({
            "image_id": str(image_id),
            "true_bin_idx": true_bin,
            "pred_bin_idx": pred_bin,
            "probs": probs,
        })
    return records


