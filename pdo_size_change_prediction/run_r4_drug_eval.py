# Outputs: CSV
# ---------------------------------------------------------------------------

def save_prob_csv(records: list[dict], out_path: Path) -> None:
    """Save per-sample softmax probabilities to CSV.

    Columns: image_id, true_bin_idx, true_bin_label, pred_bin_idx, pred_bin_label,
             followed by one column per bin named prob_{bin_label}.
    """
    prob_cols = [f"prob_{lbl}" for lbl in PDO_CHANGE_BIN_LABELS]
    fieldnames = ["image_id", "true_bin_idx", "true_bin_label",
                  "pred_bin_idx", "pred_bin_label"] + prob_cols
    with out_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for rec in records:
            row = {
                "image_id": rec["image_id"],
                "true_bin_idx": rec["true_bin_idx"],
                "true_bin_label": PDO_CHANGE_BIN_LABELS[rec["true_bin_idx"]],
                "pred_bin_idx": rec["pred_bin_idx"],
                "pred_bin_label": PDO_CHANGE_BIN_LABELS[rec["pred_bin_idx"]],
            }
            for col, p in zip(prob_cols, rec["probs"]):
                row[col] = f"{p:.6f}"
            writer.writerow(row)
    print(f"  Saved {out_path}")


def save_predictions_csv(all_records: list[dict], r4_labels: dict[str, float], out_path: Path) -> None:
    fieldnames = [
        "image_id", "patient", "drug", "day",
        "pdo_change_raw", "true_bin_idx", "true_bin_label",
        "pred_bin_idx", "pred_bin_label", "pred_confidence",
    ]
    with out_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for rec in sorted(all_records, key=lambda r: (r["image_id"])):
            cid = rec["image_id"]
            patient, drug, day = _parse_case_id(cid)
            writer.writerow({
                "image_id": cid,
                "patient": patient,
                "drug": drug,
                "day": day,
                "pdo_change_raw": f"{r4_labels.get(cid, float('nan')):.4f}",
                "true_bin_idx": rec["true_bin_idx"],
                "true_bin_label": PDO_CHANGE_BIN_LABELS[rec["true_bin_idx"]],
                "pred_bin_idx": rec["pred_bin_idx"],
                "pred_bin_label": PDO_CHANGE_BIN_LABELS[rec["pred_bin_idx"]],
                "pred_confidence": f"{float(rec['probs'][rec['pred_bin_idx']]):.4f}",
            })
    print(f"  Saved {out_path}")


# ---------------------------------------------------------------------------
# Outputs: probability distribution per drug group
# ---------------------------------------------------------------------------

def plot_patient_avg_prob_distributions(
    patient_records: dict[str, list[dict]],
    pdo_labels: dict[str, float],
    out_dir: Path,
    version: str,
) -> None:
    """One figure per patient showing mean ± std softmax distribution per drug.

    patient_records: {patient_key: list of inference records}
      where patient_key is e.g. "nyu285".
    """
    x_positions = np.arange(len(PDO_CHANGE_BIN_LABELS))

    for patient_key in sorted(patient_records.keys()):
        records = patient_records[patient_key]
        patient_num = patient_key.replace("nyu", "")

        fig, ax = plt.subplots(figsize=(11, 5))
        fig.suptitle(
            f"Avg PDO Change Probability Distribution — NYU{patient_num} [{version}]",
            fontsize=13,
        )

        # Group records by drug
        drug_to_recs: dict[str, list[dict]] = {}
        for rec in records:
            _, drug_disp, _ = _parse_case_id(rec["image_id"])
            drug_to_recs.setdefault(drug_disp, []).append(rec)

        for drug_label in DRUG_ORDER:
            drug_recs = drug_to_recs.get(drug_label, [])
            if not drug_recs:
                continue
            probs_stack = np.stack([r["probs"] for r in drug_recs], axis=0)
            mean_probs = probs_stack.mean(axis=0)
            std_probs = probs_stack.std(axis=0)
            color = DRUG_COLORS[drug_label]

            # All days share the same true bin for a given patient-drug pair
            true_bin = drug_recs[0]["true_bin_idx"]
            pdo_val = pdo_labels.get(drug_recs[0]["image_id"], float("nan"))
            legend_label = (
                f"{drug_label}  (true: {PDO_CHANGE_BIN_LABELS[true_bin]}, "
                f"PDO={pdo_val:+.1f}%)"
            )

            ax.plot(
                x_positions, mean_probs,
                marker="o", linewidth=2, markersize=5,
                color=color, label=legend_label,
            )
            ax.fill_between(
                x_positions,
                (mean_probs - std_probs).clip(0, 1),
                (mean_probs + std_probs).clip(0, 1),
                color=color, alpha=0.15,
            )
            # Star at the true bin on the mean curve
            ax.plot(
                true_bin, mean_probs[true_bin],
                marker="*", markersize=14, color=color, zorder=5,
            )
            # Subtle vertical guide at the true bin
            ax.axvline(true_bin, color=color, linestyle=":", alpha=0.35, linewidth=1)

        ax.set_xticks(x_positions)
        ax.set_xticklabels(PDO_CHANGE_BIN_LABELS, rotation=45, ha="right", fontsize=8)
        ax.set_ylim(0.0, 1.0)
        ax.set_xlabel("PDO Change Bin", fontsize=10)
        ax.set_ylabel("Mean Softmax Probability", fontsize=10)
        ax.legend(title="Drug (★ = true bin)", fontsize=8, title_fontsize=8, loc="upper right")
        ax.grid(axis="y", linestyle="--", alpha=0.3)

        fig.tight_layout()
        out_path = out_dir / f"avg_prob_distribution_{patient_key}.svg"
        fig.savefig(out_path, bbox_inches="tight")
        plt.close(fig)
        print(f"  Saved {out_path}")


# ---------------------------------------------------------------------------
# Outputs: per-drug predicted bin distribution summary plot
# ---------------------------------------------------------------------------

def plot_pred_bin_summary(
    drug_records: dict[str, list[dict]],
    r4_labels: dict[str, float],
    out_dir: Path,
    version: str,
) -> None:
    """Stacked bar showing how predicted bins are distributed per drug group."""
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    bin_count = len(PDO_CHANGE_BIN_LABELS)
    x = np.arange(bin_count)

    for ax, key, title in zip(
        axes,
        ["pred_bin_idx", "true_bin_idx"],
        ["Predicted Bin Distribution", "Ground-Truth Bin Distribution"],
    ):
        for drug_label in DRUG_ORDER:
            records = drug_records.get(drug_label, [])
            counts = np.zeros(bin_count, dtype=int)
            for rec in records:
                counts[rec[key]] += 1
            ax.bar(x, counts, label=drug_label,
                   color=DRUG_COLORS.get(drug_label, "#888"),
                   alpha=0.7, width=0.25,
                   align="center",
                   bottom=None)
        ax.set_title(title)
        ax.set_xticks(x)
        ax.set_xticklabels(PDO_CHANGE_BIN_LABELS, rotation=45, ha="right", fontsize=7)
        ax.set_ylabel("Count")
        ax.legend()
        ax.grid(axis="y", linestyle="--", alpha=0.4)

    fig.suptitle(f"PDO Change Bin Distributions by Drug Group [{version}]", fontsize=12)
    fig.tight_layout()
    out_path = out_dir / "bin_distribution_summary.svg"
    fig.savefig(out_path, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved {out_path}")


# ---------------------------------------------------------------------------
# Summary printing
# ---------------------------------------------------------------------------

def print_summary(drug_records: dict[str, list[dict]], r4_labels: dict[str, float]) -> None:
    print("\n" + "=" * 72)
    print(f"{'Sample':<30} {'True bin':<16} {'Pred bin':<16} {'Conf':>6}")
    print("-" * 72)
    for drug_label in DRUG_ORDER:
        records = drug_records.get(drug_label, [])
        correct = sum(1 for r in records if r["pred_bin_idx"] == r["true_bin_idx"])
        print(f"\n  --- {drug_label} ({len(records)} samples, exact acc = {correct}/{len(records)}) ---")
        for rec in sorted(records, key=lambda r: r["image_id"]):
            _, _, day = _parse_case_id(rec["image_id"])
            patient_str = rec["image_id"].split("nyu")[1].split("_")[0]
            short = f"NYU{patient_str}-{day}"
            raw = r4_labels.get(rec["image_id"], float("nan"))
            true_lbl = PDO_CHANGE_BIN_LABELS[rec["true_bin_idx"]]
            pred_lbl = PDO_CHANGE_BIN_LABELS[rec["pred_bin_idx"]]
            conf = float(rec["probs"][rec["pred_bin_idx"]])
            match = "✓" if rec["pred_bin_idx"] == rec["true_bin_idx"] else "✗"
            print(f"  {match} {short:<10} ({raw:+.1f}%) {true_lbl:<16} {pred_lbl:<16} {conf:.3f}")
    print("=" * 72)


