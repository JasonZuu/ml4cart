"""Evaluate pretrained PDO-change classifiers on Chip-R4 patient groups.

PDO change labels are loaded from data/On-chip_Data/pdo_change_label.json
(generated by onchip_process/build_pdo_change_label.py).

One PDO change value per (patient, drug) pair is shared across all days.

Patient groups evaluated (from data_split.json):
    test_nyu285, test_nyu318, test_nyu774

Results are grouped by drug for comparison plots.

Usage (run from repo root):
    python onchip_pdochange_prediction/run_r4_drug_eval.py --version cd8_only
    python onchip_pdochange_prediction/run_r4_drug_eval.py --version cd8_actin_ck
"""

